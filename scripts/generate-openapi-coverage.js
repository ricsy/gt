#!/usr/bin/env node

const fs = require("node:fs");
const path = require("node:path");
const { spawnSync } = require("node:child_process");
const {
  createDefaultConfig,
  fetchOfficialOpenApiJson,
  loadConfig,
  loadOpenApiJson,
} = require("./compare-api-docs");

const HTTP_METHODS = new Set(["get", "post", "put", "patch", "delete"]);
const METHOD_CONST = {
  get: "GET",
  post: "POST",
  put: "PUT",
  patch: "PATCH",
  delete: "DELETE",
};

function parseArgs(argv) {
  const options = {
    configPath: "scripts/api-doc-diff.config.json",
    live: false,
    root: process.cwd(),
  };
  for (let i = 0; i < argv.length; i++) {
    const arg = argv[i];
    if (arg === "--config") {
      options.configPath = argv[++i];
      if (!options.configPath) {
        throw new Error("--config requires a file path");
      }
    } else if (arg === "--live") {
      options.live = true;
    } else if (arg === "--root") {
      options.root = argv[++i];
      if (!options.root) {
        throw new Error("--root requires a directory path");
      }
    } else if (arg === "--help" || arg === "-h") {
      options.help = true;
    } else {
      throw new Error(`Unknown argument: ${arg}`);
    }
  }
  return options;
}

function helpText() {
  return [
    "Usage: node scripts/generate-openapi-coverage.js [options]",
    "",
    "Options:",
    "  --config <path>   Comparison config JSON (default: scripts/api-doc-diff.config.json)",
    "  --live            Fetch the configured official document before generating coverage",
    "  --root <path>     Repository root (default: current working directory)",
    "  -h, --help        Show this help",
  ].join("\n");
}

async function main(argv = process.argv.slice(2)) {
  const options = parseArgs(argv);
  if (options.help) {
    console.log(helpText());
    return 0;
  }

  const rootDir = path.resolve(options.root);
  const configPath = path.resolve(rootDir, options.configPath);
  const config = loadConfig(configPath);
  const openApi = options.live
    ? await fetchOfficialOpenApiJson(config)
    : loadOpenApiJson(path.resolve(rootDir, config.openapi.localPath));

  writeIfChanged(path.resolve(rootDir, config.go.coverageEndpointFile), renderEndpointCoverage(openApi, config));
  writeIfChanged(
    path.resolve(rootDir, config.go.compatibilityOptionsFile),
    renderCompatibilityOptions(openApi, config),
  );
  gofmt(rootDir, [
    config.go.coverageEndpointFile,
    config.go.compatibilityOptionsFile,
  ]);

  console.log(`Generated ${config.go.coverageEndpointFile}`);
  console.log(`Generated ${config.go.compatibilityOptionsFile}`);
  return 0;
}

function renderEndpointCoverage(openApi, config = createDefaultConfig()) {
  const endpoints = [];
  for (const openApiPath of Object.keys(openApi.paths || {}).sort()) {
    const pathItem = openApi.paths[openApiPath] || {};
    for (const method of Object.keys(pathItem).sort()) {
      const normalizedMethod = method.toLowerCase();
      if (!HTTP_METHODS.has(normalizedMethod)) {
        continue;
      }
      endpoints.push({
        method: METHOD_CONST[normalizedMethod],
        path: goEndpointPath(openApiPath, config),
      });
    }
  }

  return [
    "package api",
    "",
    "// Code generated by scripts/generate-openapi-coverage.js; DO NOT EDIT.",
    "// OpenAPICoverage records official OpenAPI endpoints that may not yet have dedicated high-level client helpers.",
    "// It is used by scripts/go-ast-extract.go for document coverage comparison.",
    "var OpenAPICoverage = []Endpoint{",
    ...endpoints.map((endpoint) => `\t{${endpoint.method}, ${JSON.stringify(endpoint.path)}},`),
    "}",
    "",
  ].join("\n");
}

function renderCompatibilityOptions(openApi, config = createDefaultConfig()) {
  const ignoredParameters = new Set(config.openapi.ignoredParameters || []);
  const params = new Set();

  for (const openApiPath of Object.keys(openApi.paths || {}).sort()) {
    const pathItem = openApi.paths[openApiPath] || {};
    const commonParams = Array.isArray(pathItem.parameters) ? pathItem.parameters : [];
    for (const method of Object.keys(pathItem).sort()) {
      if (!HTTP_METHODS.has(method.toLowerCase())) {
        continue;
      }
      const operation = pathItem[method] || {};
      for (const parameter of [...commonParams, ...(operation.parameters || [])]) {
        if (!parameter || !parameter.name || parameter.in === "path" || ignoredParameters.has(parameter.name)) {
          continue;
        }
        if (parameter.in === "body" && parameter.schema) {
          for (const name of schemaPropertyNames(openApi, parameter.schema)) {
            if (!ignoredParameters.has(name)) {
              params.add(name);
            }
          }
          continue;
        }
        params.add(parameter.name);
      }
    }
  }

  const typeName = config.go.compatibilityOptionsType || "OpenAPICompatibilityOptions";
  return [
    "package response",
    "",
    "// Code generated by scripts/generate-openapi-coverage.js; DO NOT EDIT.",
    "// OpenAPICompatibilityOptions records official OpenAPI parameter names that are used by generated coverage checks.",
    "// Concrete request option structs should still define typed fields when high-level client helpers are added.",
    `type ${typeName} struct {`,
    ...[...params].sort().map((name, index) => `\t${fieldName(index)} string \`json:"${name},omitempty"\``),
    "}",
    "",
  ].join("\n");
}

function goEndpointPath(openApiPath, config) {
  const prefix = config.openapi.basePathPrefix || "";
  const withoutPrefix = prefix ? openApiPath.replace(new RegExp(`^${escapeRegExp(prefix)}(?=/|$)`), "") : openApiPath;
  return withoutPrefix.replace(/\{[^}]+}/g, "%s") || "/";
}

function schemaPropertyNames(openApi, schema, seen = new Set()) {
  if (!schema || typeof schema !== "object") {
    return [];
  }
  if (schema.$ref) {
    if (seen.has(schema.$ref)) {
      return [];
    }
    seen.add(schema.$ref);
    let target = openApi;
    for (const part of schema.$ref.replace(/^#\//, "").split("/")) {
      target = target && target[part];
    }
    return schemaPropertyNames(openApi, target, seen);
  }
  if (schema.properties && typeof schema.properties === "object") {
    return Object.keys(schema.properties);
  }
  if (schema.items) {
    return schemaPropertyNames(openApi, schema.items, seen);
  }
  return [];
}

function fieldName(index) {
  return `Param${String(index + 1).padStart(3, "0")}`;
}

function writeIfChanged(filePath, content) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  if (fs.existsSync(filePath) && fs.readFileSync(filePath, "utf8") === content) {
    return;
  }
  fs.writeFileSync(filePath, content);
}

function gofmt(rootDir, files) {
  const result = spawnSync("gofmt", ["-w", ...files], {
    cwd: rootDir,
    encoding: "utf8",
  });
  if (result.error) {
    throw new Error(`Unable to run gofmt: ${result.error.message}`);
  }
  if (result.status !== 0) {
    throw new Error(`gofmt failed: ${(result.stderr || result.stdout).trim()}`);
  }
}

function escapeRegExp(value) {
  return String(value).replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

if (require.main === module) {
  main().then(
    (code) => {
      process.exitCode = code;
    },
    (error) => {
      console.error(`Error: ${error.message}`);
      process.exitCode = 2;
    },
  );
}

module.exports = {
  renderCompatibilityOptions,
  renderEndpointCoverage,
};
